const db = require("../config/db");

const InventoryModel = {
  // ‚úÖ ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ ‡πÅ‡∏•‡∏∞‡∏Å‡∏£‡∏≠‡∏á‡∏ï‡∏≤‡∏°‡∏´‡∏°‡∏ß‡∏î‡∏´‡∏°‡∏π‡πà
  async getMaterials({ search = "%", category = "%", limit = 10, offset = 0 }) {
    try {
      console.log("üîç Fetching materials with search:", search, "category:", category);

      const [[{ total }]] = await db.query(
        `SELECT COUNT(*) AS total 
         FROM materials 
         WHERE name LIKE ? 
         AND (category_id = ? OR ? = '%')`,
        [search, category, category]
      );

      const [rows] = await db.query(
        `SELECT 
          m.material_id, 
          m.name AS material_name, 
          c.category_name, 
          COALESCE(MIN(ib.received_date), m.received_date, 'N/A') AS received_date, 
          COALESCE(
            MIN(ib.expiration_date), 
            DATE_ADD(COALESCE(MIN(ib.received_date), m.received_date), INTERVAL sl.shelf_life_days DAY), 
            'N/A'
          ) AS expiration_date, 
          u.unit_name,
          SUM(m.stock) AS total_quantity,
          CASE 
            WHEN SUM(m.stock) <= 0 THEN '‡∏´‡∏°‡∏î'
            WHEN SUM(m.stock) <= m.min_stock THEN '‡∏ï‡πà‡∏≥‡∏Å‡∏ß‡πà‡∏≤‡∏Å‡∏≥‡∏´‡∏ô‡∏î'
            WHEN COALESCE(MIN(ib.expiration_date), 
              DATE_ADD(COALESCE(MIN(ib.received_date), m.received_date), INTERVAL sl.shelf_life_days DAY)
            ) <= CURDATE() THEN '‡∏´‡∏°‡∏î‡∏≠‡∏≤‡∏¢‡∏∏‡πÅ‡∏•‡πâ‡∏ß'
            WHEN COALESCE(MIN(ib.expiration_date), 
              DATE_ADD(COALESCE(MIN(ib.received_date), m.received_date), INTERVAL sl.shelf_life_days DAY)
            ) BETWEEN CURDATE() AND DATE_ADD(CURDATE(), INTERVAL 7 DAY) THEN '‡πÉ‡∏Å‡∏•‡πâ‡∏´‡∏°‡∏î‡∏≠‡∏≤‡∏¢‡∏∏'
            ELSE '‡∏õ‡∏Å‡∏ï‡∏¥'
          END AS status
        FROM materials m
        LEFT JOIN categories c ON m.category_id = c.category_id
        LEFT JOIN inventory_batches ib ON m.material_id = ib.material_id
        LEFT JOIN unit u ON m.unit_id = u.unit_id
        LEFT JOIN shelf_life sl ON m.category_id = sl.category_id
        WHERE m.name LIKE ? 
          AND (m.category_id = ? OR ? = '%')
        GROUP BY m.material_id, m.name, c.category_name, u.unit_name, m.received_date, m.min_stock, sl.shelf_life_days
        ORDER BY m.material_id ASC
        LIMIT ? OFFSET ?;`,
        [search, category, category, limit, offset]
      );

      console.log("‚úÖ Materials retrieved successfully");
      return { total, rows };
    } catch (error) {
      console.error("‚ùå Error fetching materials:", error);
      throw new Error("‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡πÑ‡∏î‡πâ");
    }
  },

  // ‚úÖ ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡∏ï‡∏≤‡∏° ID
  async getMaterialById(id) {
    try {
      console.log("üîç Fetching material by ID:", id);
      const [rows] = await db.query(
        `SELECT m.*, u.unit_name FROM materials m
         LEFT JOIN unit u ON m.unit_id = u.unit_id
         WHERE m.material_id = ?`,
        [id]
      );

      if (rows.length === 0) {
        console.warn(`‚ö†Ô∏è Material ID ${id} not found`);
        return null;
      }

      console.log("‚úÖ Material retrieved:", rows[0]);
      return rows[0];
    } catch (error) {
      console.error("‚ùå Error fetching material by ID:", error);
      throw new Error("‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡πÑ‡∏î‡πâ");
    }
  },

  // ‚úÖ ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡πÉ‡∏´‡∏°‡πà
  async addMaterial({ name, category_id, quantity, received_date, expiration_date, price, unit_id }) {
    console.log("‚úÖ Received unit_id:", unit_id);

    if (!unit_id) {
      throw new Error("‚ùå unit_id ‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á ‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ‡∏£‡∏±‡∏ö‡∏Ñ‡πà‡∏≤");
    }

    const connection = await db.getConnection();
    try {
      await connection.beginTransaction();

      // ‚úÖ ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡∏•‡∏á‡πÉ‡∏ô `materials`
      const [insertResult] = await connection.query(
        `INSERT INTO materials (name, category_id, unit_id) VALUES (?, ?, ?)`,
        [name, category_id, unit_id]
      );
      const materialId = insertResult.insertId;

      // ‚úÖ ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏á‡πÉ‡∏ô `inventory_batches`
      await this.addInventoryBatch(connection, {
        material_id: materialId,
        quantity,
        received_date,
        expiration_date,
        price,
      });

      await connection.commit();
      console.log("‚úÖ Material added successfully:", materialId);
      return materialId;
    } catch (error) {
      await connection.rollback();
      console.error("‚ùå Error adding material:", error);
      throw new Error("‚ùå ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à");
    } finally {
      connection.release();
    }
  },

  // ‚úÖ ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏•‡πá‡∏≠‡∏ï‡∏Ç‡∏≠‡∏á‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö
  async addInventoryBatch(connection, { material_id, quantity, received_date, expiration_date, price }) {
    try {
      console.log("üìù Adding to inventory_batches:", {
        material_id,
        quantity,
        received_date,
        expiration_date,
        price,
      });

      await connection.query(
        `INSERT INTO inventory_batches (material_id, quantity, received_date, expiration_date, price) 
         VALUES (?, ?, ?, ?, ?)`,
        [material_id, quantity, received_date, expiration_date, price]
      );

      console.log("‚úÖ Inventory batch added successfully");
    } catch (error) {
      console.error("‚ùå Error adding inventory batch:", error);
      throw new Error("‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏á‡πÉ‡∏ô inventory_batches ‡πÑ‡∏î‡πâ");
    }
  },

  // ‚úÖ ‡∏î‡∏∂‡∏á unit_id ‡∏à‡∏≤‡∏Å‡∏ä‡∏∑‡πà‡∏≠‡∏´‡∏ô‡πà‡∏ß‡∏¢
  async getUnitId(unitName) {
    try {
      console.log(`üîç Searching for unit_id of '${unitName}'...`);

      const [rows] = await db.query("SELECT unit_id FROM unit WHERE unit_name = ?", [unitName]);

      if (rows.length === 0) {
        console.error(`‚ùå Unit '${unitName}' not found in database`);
        throw new Error(`‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏´‡∏ô‡πà‡∏ß‡∏¢ '${unitName}' ‡πÉ‡∏ô‡∏ê‡∏≤‡∏ô‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•`);
      }

      console.log(`‚úÖ Found unit_id: ${rows[0].unit_id} for unit '${unitName}'`);
      return rows[0].unit_id;
    } catch (error) {
      console.error("‚ùå Error fetching unit_id:", error);
      throw new Error("‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• unit_id ‡πÑ‡∏î‡πâ");
    }
  },

  // ‚úÖ ‡∏•‡∏ö‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö
  async deleteMaterial(id) {
    try {
      console.log(`üóëÔ∏è Deleting material ID: ${id}`);
      await db.query("DELETE FROM materials WHERE material_id = ?", [id]);
      console.log("‚úÖ Material deleted successfully");
      return true;
    } catch (error) {
      console.error("‚ùå Error deleting material:", error);
      throw new Error("‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏•‡∏ö‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡πÑ‡∏î‡πâ");
    }
  },

  // ‚úÖ ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡πá‡∏≠‡∏ï‡∏Ç‡∏≠‡∏á‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö
  async getMaterialBatches(materialId) {
    try {
      console.log(`üîç Fetching batches for material ID: ${materialId}`);
      const [rows] = await db.query(
        `SELECT batch_id, received_date, expiration_date, quantity 
         FROM inventory_batches 
         WHERE material_id = ? 
         ORDER BY received_date DESC`,
        [materialId]
      );

      console.log("‚úÖ Retrieved material batches:", rows);
      return rows;
    } catch (error) {
      console.error("‚ùå Error fetching material batches:", error);
      throw new Error("‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡πá‡∏≠‡∏ï‡∏Ç‡∏≠‡∏á‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡πÑ‡∏î‡πâ");
    }
  },

  // ‚úÖ ‡∏•‡∏ö‡∏•‡πá‡∏≠‡∏ï‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö
  async deleteBatch(batchId) {
    try {
      console.log(`üóëÔ∏è Deleting batch ID: ${batchId}`);
      await db.query("DELETE FROM inventory_batches WHERE batch_id = ?", [batchId]);
      console.log("‚úÖ Batch deleted successfully");
    } catch (error) {
      console.error("‚ùå Error deleting batch:", error);
      throw new Error("‚ùå ‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏•‡∏ö‡∏•‡πá‡∏≠‡∏ï‡∏Ç‡∏≠‡∏á‡∏ß‡∏±‡∏ï‡∏ñ‡∏∏‡∏î‡∏¥‡∏ö‡πÑ‡∏î‡πâ");
    }
  },
};

module.exports = InventoryModel;
